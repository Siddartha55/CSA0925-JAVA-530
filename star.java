import java.util.*;
class star{
	public static void main(String[] args){
		int n,i,j;
		Scanner s=new Scanner(System.in);
		System.out.println("enter the no of rows : ");
		n=s.nextInt();
		for(i=0;i<n;i++){
			
			for(j=0;j<=i;j++){

				System.out.print("*");
				}
			System.out.println();
			}
		}
	}